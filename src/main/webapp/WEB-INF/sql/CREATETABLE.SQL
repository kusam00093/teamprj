CREATE TABLE USERS (
   USER_ID VARCHAR2(30)   PRIMARY KEY,   
   USER_NAME VARCHAR2(20) NOT NULL,
   USER_PASSWD VARCHAR2(30) NOT NULL,
   USER_EMAIL VARCHAR2(300) NOT NULL,
   USER_ADDRESS VARCHAR2(300) NOT NULL,
   USER_PHONE VARCHAR2(30) NOT NULL,
   USER_SOCIAL_NUM VARCHAR2(50) NOT NULL,
   INDATE DATE DEFAULT SYSDATE 
);

CREATE TABLE COMPANY (
   COM_ID VARCHAR2(30)          PRIMARY KEY
   ,COM_PASSWD VARCHAR2(30)     NOT NULL
   ,COM_TYPE VARCHAR2(100)      NOT NULL
   ,COM_NUM VARCHAR2(30)        NOT NULL
   ,COM_NAME VARCHAR2(300)      NOT NULL
   ,COM_ADDRESS VARCHAR2(300)   NOT NULL
   ,COM_CEO VARCHAR2(20)        NOT NULL
   ,COM_PHONE VARCHAR2(50)      NOT NULL 
   ,COM_EMAIL VARCHAR2(300)     NOT NULL
    ,INDATE DATE DEFAULT SYSDATE
);

CREATE TABLE RESUME (
    USER_ID  VARCHAR2(30),
 FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
   ,RESUME_RNO  NUMBER(22) PRIMARY KEY
    ,RESUME_PROFILE VARCHAR2(300)
    ,RESUME_CONTENT_SELF VARCHAR2(2000)
    ,RESUME_CONTENT_MOTIVATION VARCHAR2(2000) 
);
CREATE TABLE RESUME_CAREER(
    RESUME_RNO  NUMBER(22),
     FOREIGN KEY (RESUME_RNO) REFERENCES RESUME(RESUME_RNO)
    ,RESUME_CAREER_COMPANY VARCHAR2(3000) 
    ,RESUME_CAREER_STARTDATE DATE
    ,RESUME_CAREER_ENDDATE DATE
    ,RESUME_CAREER_DEPARTMENT VARCHAR2(3000)
    );

CREATE TABLE RESUME_GRADE(
    RESUME_RNO  NUMBER(22),
     FOREIGN KEY (RESUME_RNO) REFERENCES RESUME(RESUME_RNO)
    ,RESUME_GRADE_UNIVERSE VARCHAR2(300)
    ,RESUME_GRADE_STATE VARCHAR2(30)
    ,RESUME_GRADE_CONTENT VARCHAR2(300)
    );
 
 CREATE TABLE RESUME_LICENSE(
    RESUME_RNO  NUMBER(22),
     FOREIGN KEY (RESUME_RNO) REFERENCES RESUME(RESUME_RNO)
    ,RESUME_LICENSE_CONTENT VARCHAR2(3000)
    );

    
    

CREATE TABLE POSTING(
    COM_ID  VARCHAR2(30),
    FOREIGN KEY (COM_ID) REFERENCES COMPANY(COM_ID)
    ,POSTING_PNO NUMBER(22) PRIMARY KEY
   ,POSTING_CAREER VARCHAR2(30)
   ,POSTING_GRADE VARCHAR2(30)
   ,POSTING_HOPE_DEPARTMENT VARCHAR2(30)
  
   ,POSTING_CONTENT VARCHAR2(2000)
   ,POSTING_TITLE VARCHAR2(2000)
   ,POSTING_STARTDATE DATE DEFAULT SYSDATE
    ,POSTING_ENDDATE DATE
   ,POSTING_COM_FROFILE VARCHAR2(2000)    
);

CREATE TABLE POSTING_STACK(
    POSTING_PNO  NUMBER(22),
    FOREIGN KEY (POSTING_PNO) REFERENCES POSTING(POSTING_PNO)
    ,POSTING_STACK VARCHAR2(300)
    );
    

CREATE TABLE BOARD (
    USER_ID  VARCHAR2(30),
 FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
   ,BOARD_TITLE VARCHAR2(300)
   ,BOARD_CONENT VARCHAR2(2000)
   ,BOARD_HIT NUMBER(22)
   ,BOARD_INDATE DATE DEFAULT SYSDATE
   ,BOARD_BNO NUMBER(22)
);

CREATE TABLE USER_BOOKMARK(
    POSTING_PNO  NUMBER(22),
    FOREIGN KEY (POSTING_PNO) REFERENCES POSTING(POSTING_PNO)
    ,USER_ID  VARCHAR2(30)
    ,FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
   ,UB_UBNO NUMBER(22)
   ,UB_BOOLEAN BOOLEAN
);

CREATE TABLE USER_BOOKMARK(
    POSTING_PNO NUMBER(22),
    USER_ID VARCHAR2(30),
    UB_UBNO NUMBER(22),
    UB_BOOLEAN NUMBER(1),
    FOREIGN KEY (POSTING_PNO) REFERENCES POSTING(POSTING_PNO),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);

CREATE TABLE COMPANY_BOOKMARK(
    RESUME_RNO  NUMBER(22),
     FOREIGN KEY (RESUME_RNO) REFERENCES RESUME(RESUME_RNO),
    COM_ID  VARCHAR2(30),
    FOREIGN KEY (COM_ID) REFERENCES COMPANY(COM_ID)
   ,CB_CBNO NUMBER(22)
   ,CB_BOOLEAN NUMBER(1)
);

CREATE TABLE USER_STACK (
    USER_ID  VARCHAR2(30)
    ,FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
   ,USER_STACK_NAME VARCHAR2(300)
);


CREATE TABLE APPLICANT_RECODE (  
    POSTING_PNO  NUMBER(22),
    FOREIGN KEY (POSTING_PNO) REFERENCES POSTING(POSTING_PNO)
   ,APPLICANT_RECODE_RESULT NUMBER(1)
   ,APPLICANT_RECODE_ID NUMBER(22) PRIMARY KEY
    ,RESUME_RNO  NUMBER(22),
     FOREIGN KEY (RESUME_RNO) REFERENCES RESUME(RESUME_RNO)
);



